'''
Na divisão sem casa decimal usa-se no python // e apenas uma / para 
resultado de número flutuante.
'''

adicao = 10 + 10
print('Adição', adicao)

subtracao = 10 - 5
print('Subtração', subtracao)

multiplicacao = 10 * 10
print('Multiplicação', multiplicacao)

divisao = 10 / 3  # float
print('Divisão', divisao)

divisao_inteira = 10 // 3 #float
print('Divisão inteira', divisao_inteira)

exponenciacao = 2 ** 10 # numeros elevados dois elevado a dez
print('Exponenciação', exponenciacao)

modulo = 55 % 2  # resto da divisão
print('Módulo', modulo)

print(10 % 8 == 0) # ver se é divisiver por 8
print(16 % 8 == 0) # ver se é divisiver por 8
print(10 % 2 == 0) # ver se o numero e par ou não
print(15 % 2 == 0) # ver se o numero e par ou não
print(16 % 2 == 0) # ver se o numero e par ou não