'''
Python = Linguagem de programação
Tipo de tipagem = Dinamica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
'''
# Podem ser aspas simples ou duplas 

# Aspas simples
print('Guilherme Almeida')
print(1, 'Guilherme "Almeida"')

# Aspas duplas
print("Guilherme Almeida")
print(2, "Guilherme 'Almeida'")

# Escape serve para ignorar as aspas
print('Guilherme \"Almeida\"')

# r
print(r'Guilherme \"Almeida\"')